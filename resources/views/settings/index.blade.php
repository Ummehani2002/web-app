        <div class="toolbar">
            <button id="generate-btn" class="btn btn-primary" type="button">Generate token</button>
            <button id="copy-btn" class="btn" type="button">Copy token</button>
        </div>
        <input id="token-output" class="token" type="text" value="{{ $token?->access_token ?? '' }}" readonly>
        <p id="status" class="help">
            @if($token)
                Last generated at {{ $token->created_at?->format('d M Y H:i:s') }}. Expires at {{ $token->expires_at?->format('d M Y H:i:s') }}.
            @else
                No token generated yet.
            @endif
        </p>
        <p id="remaining" class="help">
            @if($token)
                Seconds remaining: {{ $token->secondsRemaining() }}.
            @endif
        </p>
        <a class="back" href="{{ route('dashboard') }}">Back to Dashboard</a>
