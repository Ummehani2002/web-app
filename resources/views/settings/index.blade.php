<script>
    const csrfToken = document.querySelector('meta[name="csrf-token"]')?.content ?? '';
    const generateBtn = document.getElementById('generate-btn');
    const copyBtn = document.getElementById('copy-btn');
    const tokenOutput = document.getElementById('token-output');
    const statusEl = document.getElementById('status');
    const remainingEl = document.getElementById('remaining');
    let intervalId = null;

    const clearTicker = () => {
        if (intervalId) {
            clearInterval(intervalId);
            intervalId = null;
        }
    };

    const startTicker = (seconds) => {
        clearTicker();
        let remaining = Number(seconds) || 0;
        const tick = () => {
            if (remaining <= 0) {
                clearTicker();
                remainingEl.textContent = 'Token expired.';
                return;
            }
            remainingEl.textContent = `Seconds remaining: ${remaining}.`;
            remaining -= 1;
        };
        tick();
        intervalId = setInterval(tick, 1000);
    };

    generateBtn.addEventListener('click', async () => {
        statusEl.textContent = 'Generating token...';
        remainingEl.textContent = '';
        clearTicker();
        try {
            const response = await fetch("{{ route('settings.token.generate') }}", {
                method: 'POST',
                headers: {
                    'X-CSRF-TOKEN': csrfToken,
                    Accept: 'application/json',
                },
            });
            const payload = await response.json().catch(() => ({}));
            if (!response.ok || payload?.status !== true) {
                throw new Error(payload?.message || 'Token generation failed.');
            }

            tokenOutput.value = payload.full_token || '';
            statusEl.textContent = `Generated by ${payload.generated_by ?? '-'} at ${payload.generated_at_human ?? '-'}. Expires at ${payload.expires_at_human ?? '-'}.`;
            startTicker(payload.seconds_remaining || 0);
        } catch (error) {
            statusEl.textContent = error.message;
        }
    });

    copyBtn.addEventListener('click', async () => {
        if (!tokenOutput.value.trim()) {
            statusEl.textContent = 'No token to copy.';
            return;
        }
        try {
            await navigator.clipboard.writeText(tokenOutput.value.trim());
            statusEl.textContent = 'Token copied.';
        } catch (error) {
            statusEl.textContent = 'Copy failed.';
        }
    });

    @if($token)
    startTicker({{ $token->secondsRemaining() }});
    @endif
</script>
